public class ElectricFurnaceBlock extends AbstractMachineBlock {

    @Override
    public BlockState getStateForPlacement(BlockItemUseContext context) {
        return this.getDefaultState().with(BlockStateProperties.HORIZONTAL_FACING, context.getPlacementHorizontalFacing().getOpposite())
                .with(BlockStateProperties.LIT, false);
    }

    @Override
    protected void fillStateContainer(StateContainer.Builder<Block, BlockState> builder) {
        builder.add(BlockStateProperties.HORIZONTAL_FACING, BlockStateProperties.LIT);
    }

    @Override
    public ElectricFurnaceTileEntity createTileEntity(BlockState state, IBlockReader world) {
        return new ElectricFurnaceTileEntity();
    }
}
